            <h2>LE RESEAU DE EST : </h2>
             Les EST  (Ecoles Supérieures de Technologies) sont un réseau d’établissement dans diverses villes marocaines, les EST sont spécialisées dans la formation technique, la durée d’études à l’EST dure 2 ans (4 semestres). On compte des EST dans plusieurs villes : Oujda, Agadir, Casablanca, Meknes, Salé…etc.

<h2>Diplôme de l’EST</h2>
Le diplôme de l’EST permet au lauréat de l’Ecole Supérieure de Technologie de travailler dans une entreprise, ou bien de continuer dans un autre établissement supérieur. La filière du diplôme obtenu à l’EST peut différer selon les filières proposées par l’Ecole concernée.

 <h2>Filières de l’EST</h2>
Les filières diffèrent d’une EST à une autre, les Ecoles Supérieures de Technologies offrent des filières de formation adaptées aux besoin de marché et à la demande des entreprises. Par exemple à l’EST Agadir on compte des filières telle que : Génie Informatique, Génie Electrique, Techniques de Management, Technicien Réseau…etc. A l’EST Casa c’est : Gestion des Entreprise et des Administrations, Finance comptabilité, Génie mécanique…et bien d’autres ! Il faut choisir son EST en fonction de la filière qu’on vise.

<h2>Accès à l’EST</h2>
Pour accéder à l’Ecole Supérieure de Technologie, il faut postuler pour chaque école dès qu’elle ouvre les portes à candidature. La candidature pour les EST commence généralement vers le mois de Mai et s’arrête en fin Juin. Il se peut qu’il s’agisse d’envoyer votre dossier par poste (pour certaines EST) et pour d’autre il suffit de s’inscrire sur internet.

L’accès à l’EST est sans concours, ça veut dire que les Ecoles Supérieures de Technologies sont accessibles juste par sélection en se basant sur la note du baccalauréat.

Liste principale et liste d’attente EST
Il y’a toujours une liste principale à l’EST et une ou plusieurs listes d’attentes, par ce que la plupart des gens qui sont en liste principale ne viennent pas le jour de l’inscription, ce qui laisse la chance aux candidats de la liste d’attente, donc lorsqu’on est dans la liste d’attente de l’EST il y’a de très fortes chances à ce qu’on soit admis. Il faut juste suivre quand est ce que la liste d’attente EST est annoncée ainsi que son inscription.

<h2>Seuils de l’EST</h2>
Chaque année les élèves se posent des questions à propos du seuil EST, mais généralement il n’y a pas de seuils précis pour accéder à l’EST, vu que c’est la sélection des élèves qui détermine finalement qui est le dernier ayant été sélectionné pour chaque filière, et donc le seuil déterminé.

