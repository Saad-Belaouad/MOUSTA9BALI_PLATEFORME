           <h2>INAS</h2>
            INAS Tanger est un établissement qui forme des profils de travailleurs sociaux dotés d’un ensemble de méthodes et techniques pour intervenir dans le domaine social, et qui  a été crée en 1981 par l’autorité gouvernementale chargée des affaires sociales, avec l’appui de l’agence des  États-Unis pour le Développement International (l’USAID).
<br>
L’INAS - Institut National de l’Action Sociale est un établissement supérieur créé en 1981 à Tanger. L’INAS assure la formation et le perfectionnement des personnels destinés à travailler dans le domaine de l’Action Sociale, qui seront affectés dans les administrations publiques, les collectivités locales et dans les organismes semi-publics et privés.
<br>
L’INAS Tanger contribue également à l’élaboration et à la vulgarisation des techniques et méthodes de travail et de recherches dans le domaine de l’Action Sociale.
<br>
L’enseignement dispensé à l’INAS comprend un premier cycle de deux ans pour la formation des conseillers sociaux. Un deuxième cycle est mis en place pour la formation des gestionnaires en affaires sociales.

