            L’ERSSM Rabat – Ecole Royale du Service de Santé Militaire est un établissement relevant du secteur militaire au Maroc, dont la vocation est de former des médecins, spécialistes et chirurgiens, ainsi que des infirmiers. Qui sont destinés à travailler avec les forces armées royales.

L’accès à l’ERSSM Rabat est ouvert aux bacheliers scientifiques, sur présélection et concours écrit. La durée d’études et les cours assurés sont similaires à ceux des autres facultés de médecine, avec un surplus de formation militaire pour préparer les futurs médecins au travail sur terrain et aux missions qui leur seront confiées.
            <h2>CONDITION D'ACCES & Fillieres:</h2>
             L’accès à l’ERSSM Rabat est ouvert aux bacheliers scientifiques, sur présélection et concours écrit. La durée d’études et les cours assurés sont similaires à ceux des autres facultés de médecine, avec un surplus de formation militaire pour préparer les futurs médecins au travail sur terrain et aux missions qui leur seront confiées.

le nombre d’étudiants admis chaque année est un peu limité, mais la sélection donne la chance à un grand nombre de bacheliers de passer le concours de l’ERSSM, il s’agit d’un concours à concurrence accrue.

L’Ecole Royale du Service de Santé Médicale est une école à vocation militaire, il est donc nécessaire de remplir certaines conditions de base, à savoir : être de nationalité marocaine, célibataire, apte à la visite médicale et avoir un casier judiciaire vierge.

Plus spécifiquement, il existe quatre filières :

-Médecine générale avec un cursus de 7 ans

-Médecine dentaire avec un cursus de 5 ans

-Médecine vétérinaire avec un cursus de 6 ans

-Pharmacie avec un cursus de 2 ans

Dans tous les cas, une présélection est effectuée sur la base du dossier de candidature, suivie d’un concours d’admission. Pour s’assurer un dossier complet, acceptable lors de la phase de présélection. Les dossiers sont à envoyer à : Monsieur le Directeur de l’École Royale du Service de Santé Militaire (ERSSM), Avenue El-Hachimi El-Mastari N°1 RABAT.

Les candidats qui postulent aux formations générale, dentaire ou vétérinaire doivent constituer un dossier comportant les documents suivants : une demande manuscrite, un relevé de notes, un certificat de scolarité, un extrait récent d’acte de naissance, des photocopies légalisées des CIN du candidat et de son tuteur, ainsi que quatre enveloppes timbrées avec enveloppe.

Les candidats qui postulent à la formation en pharmacie doivent en outre joindre : une attestation originale de réussite au 4ème semestre de la filière, un relevé de notes des 1ère et 2ème années et une photocopie légalisée du DEUG.
             
             
